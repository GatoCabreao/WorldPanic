    love.graphics.draw(bg_image,(16*scale),(16*scale),0,scale,scale)
       
    for mapn=1,800 do
      
      if      Level[mapn] == 1 then love.graphics.draw(brick_image, mapx * (16*scale), mapy * (16*scale),0,scale,scale)    
